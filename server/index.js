// API endpoint for contact form
app.post('/api/contact', async (req, res) => {
  const { firstName, lastName, email, phone, city, country, service, message } = req.body;
  
  // Validate required fields including phone, city, and country
  if (!firstName || !lastName || !email || !phone || !city || !country || !service || !message) {
    return res.status(400).json({ 
      success: false, 
      message: 'Please fill all required fields' 
    });
  }
  
  // Map service code to service name
  const serviceNames = {
    'NGO-grants': 'Government & NGO Grants',
    'Donations': 'Donations',
    'Development-Funds': 'Development Funds',
    'crowdfunding': 'Crowdfunding & Grantmaking',
    'Agri-Business': 'Agri-Business Financing',
    'No-Guarantee-Loans': 'No-Guarantee Loans',
    'Micro-Finance': 'Micro Financing Services',
    'Business-Loans': 'Business Loans',
    'Real-Estate': 'Real Estate Financing',
    'other': 'Other Services'
  };
  
  const serviceName = serviceNames[service] || service;
  
  // Get country name from code
  let countryName = country;
  if (country) {
    const countryObj = countries.find(c => c.code === country);
    if (countryObj) {
      countryName = countryObj.name;
    }
  }
  
  try {
    // Set up email options
    const mailOptions = {
      from: `"${firstName} ${lastName}" <${process.env.ZOHO_EMAIL}>`,
      to: process.env.ZOHO_EMAIL,
      cc: process.env.ZOHO_CC_EMAIL,
      replyTo: email,
      subject: `New Inquiry: ${serviceName}`,
      text: `
        New Contact Form Submission
        
        Name: ${firstName} ${lastName}
        Email: ${email}
        Phone: ${phone}
        Location: ${city}, ${countryName}
        Service of Interest: ${serviceName}
        
        Message:
        ${message}
        
        This is an automated email from your website contact form.
      `,
      html: `
        <div style="font-family: Arial, sans-serif; line-height: 1.6; color: #333; max-width: 600px; margin: 0 auto;">
          <div style="background-color: #fe6700; color: white; padding: 10px 20px; border-radius: 5px 5px 0 0;">
            <h2>New Contact Form Submission</h2>
          </div>
          <div style="padding: 20px; border: 1px solid #ddd; border-top: none; border-radius: 0 0 5px 5px;">
            <p>You have received a new inquiry from your website contact form.</p>
            
            <div style="margin-bottom: 10px;">
              <span style="font-weight: bold;">Name:</span> ${firstName} ${lastName}
            </div>
            <div style="margin-bottom: 10px;">
              <span style="font-weight: bold;">Email:</span> ${email}
            </div>
            <div style="margin-bottom: 10px;">
              <span style="font-weight: bold;">Phone:</span> ${phone}
            </div>
            <div style="margin-bottom: 10px;">
              <span style="font-weight: bold;">Location:</span> ${city}, ${countryName}
            </div>
            <div style="margin-bottom: 10px;">
              <span style="font-weight: bold;">Service of Interest:</span> ${serviceName}
            </div>
            <div style="margin-bottom: 10px;">
              <span style="font-weight: bold;">Message:</span><br>
              ${message.replace(/\n/g, '<br>')}
            </div>
          </div>
          <div style="margin-top: 20px; font-size: 12px; color: #777;">
            <p>This is an automated email from your website contact form.</p>
          </div>
        </div>
      `
    };
    
    // Send the email
    await transporter.sendMail(mailOptions);
    
    // Return success response
    res.status(200).json({ 
      success: true, 
      message: 'Your message has been sent successfully!' 
    });
  } catch (error) {
    console.error('Email error:', error);
    res.status(500).json({ 
      success: false, 
      message: 'Failed to send email. Please try again later.' 
    });
  }
});

